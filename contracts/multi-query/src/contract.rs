use oorandom::Rand32;

#[cfg(not(feature = "library"))]
use cosmwasm_std::entry_point;
use cosmwasm_std::{to_binary, Binary, Deps, DepsMut, Env, MessageInfo, Response, StdResult};
use cw2::set_contract_version;

use crate::error::ContractError;
use crate::msg::{
    ExecuteMsg, InstantiateMsg, QueryMsg, QueryMultiTestResponse, QueryTestResponse,
    RandomResponse, RuleResponse,
};

// version info for migration info
const CONTRACT_NAME: &str = "crates.io:multi-query";
const CONTRACT_VERSION: &str = env!("CARGO_PKG_VERSION");

#[cfg_attr(not(feature = "library"), entry_point)]
pub fn instantiate(
    deps: DepsMut,
    _env: Env,
    info: MessageInfo,
    _msg: InstantiateMsg,
) -> Result<Response, ContractError> {
    set_contract_version(deps.storage, CONTRACT_NAME, CONTRACT_VERSION)?;

    Ok(Response::new()
        .add_attribute("method", "instantiate")
        .add_attribute("owner", info.sender))
}

#[cfg_attr(not(feature = "library"), entry_point)]
pub fn execute(
    deps: DepsMut,
    _env: Env,
    info: MessageInfo,
    msg: ExecuteMsg,
) -> Result<Response, ContractError> {
    match msg {
        ExecuteMsg::QueryResult {} => query_result(deps, info),
    }
}

#[cfg_attr(not(feature = "library"), entry_point)]
pub fn query(deps: Deps, env: Env, msg: QueryMsg) -> StdResult<Binary> {
    match msg {
        QueryMsg::GetRandom {} => to_binary(&query_random(env)?),
        QueryMsg::GetBoolBinary {} => to_binary(&query_bool_binary()?),
        QueryMsg::QueryTest {} => to_binary(&query_test(deps, env)?),
        QueryMsg::QueryMultiTest {} => to_binary(&query_multi_test(deps, env)?),
    }
}

pub fn query_result(_deps: DepsMut, _info: MessageInfo) -> Result<Response, ContractError> {
    Ok(Response::new().add_attribute("method", "query_result"))
}

fn query_random(env: Env) -> StdResult<RandomResponse> {
    let mut rng = Rand32::new(env.block.height);
    Ok(RandomResponse {
        number: rng.rand_u32(),
    })
}

fn query_bool_binary() -> StdResult<RuleResponse<Option<Binary>>> {
    Ok((true, None))
}

fn query_test(deps: Deps, env: Env) -> StdResult<QueryTestResponse> {
    let msg = QueryMsg::GetRandom {};
    let res: RuleResponse<Option<Binary>> =
        deps.querier.query_wasm_smart(&env.contract.address, &msg)?;
    let data = format!("{:?}", res.0);
    Ok(QueryTestResponse { data })
}

fn query_multi_test(_deps: Deps, _env: Env) -> StdResult<QueryMultiTestResponse> {
    Ok(QueryMultiTestResponse { data: vec![] })
}

